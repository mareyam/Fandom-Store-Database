CREATE DATABASE ShoppingSite
USE ShoppingSite

CREATE TABLE Customer(
	Customer_ID				INT IDENTITY(121000,1) PRIMARY KEY ,
	UserName				VARCHAR(25) NOT NULL UNIQUE,
	EmailAddress			VARCHAR(30) NOT NULL UNIQUE CHECK(EmailAddress LIKE '%@%.%'), --Email has to have @ and . in it
	UserPassword			VARCHAR(25) NOT NULL CHECK(LEN(UserPassword) > 7) , --Password cannot have less then 7 characters
	FirstName				VARCHAR(25) NOT NULL,
	LastName				VARCHAR(25) NOT NULL,
	PhoneNumber				VARCHAR(17) NOT NULL CHECK(LEN(PhoneNumber) >= 7), --Smallest phone number in world has 7 digits and longest 15
	DateOfBirth				DATE NOT NULL,
	Gender					VARCHAR(1) NOT NULL CHECK(Gender IN('F','M','O')), --Only 3 gender options allowed allowed
	CartLastUpdate			DATE DEFAULT GETDATE(),
	WishListLastUpdate		DATE DEFAULT GETDATE(),

);

CREATE TABLE CustomerAddress(
	Address_ID		INT IDENTITY(320000,1) PRIMARY KEY,
	StreetAddress	VARCHAR (45) NOT NULL,
	Area			VARCHAR(35) NOT NULL,
	City			VARCHAR(25),
	Country			VARCHAR(25) NOT NULL,
	AddressLabel	VARCHAR(9) CHECK(AddressLabel IN('Home','Office','Holiday')),--Only three kind of labels allowed
	Customer_ID		INT FOREIGN KEY REFERENCES Customer(Customer_ID) ON DELETE CASCADE --Deletes address when customer gets deleted
);

CREATE TABLE Product(
	Product_ID			INT	IDENTITY(219000,1) PRIMARY KEY,
	Title				VARCHAR(150) NOT NULL UNIQUE,
	Price				INT	NOT NULL CHECK(Price > 0),
	Discount			INT	CHECK(Discount > 0),
	Quantity			INT NOT NULL CHECK(Quantity > 0),
	Specifications		VARCHAR(512),
	SelfNotes			VARCHAR(255),
	ProductDescription	VARCHAR(512),
	DateAdded			Date DEFAULT GETDATE(),
	DateUpdated			Date DEFAULT GETDATE()		
);

CREATE TABLE Category(
	Category_ID			INT IDENTITY(1,1) PRIMARY KEY,
	CategoryName		VARCHAR(35)	NOT NULL UNIQUE,
	CategoryDescription	VARCHAR(512)
);

CREATE TABLE ProductCategory(
	Category_ID			INT,
	Product_ID			INT,
	FOREIGN KEY(Category_ID) REFERENCES Category(Category_ID) ON DELETE CASCADE,
	FOREIGN KEY(Product_ID) REFERENCES Product(Product_ID) ON DELETE CASCADE,
	PRIMARY KEY(Category_ID, Product_ID),
);

CREATE TABLE WishlistItems(
	Customer_ID		INT,
	Product_ID		INT,
	PRIMARY KEY(Customer_ID, Product_ID),
	FOREIGN KEY(Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE,
	FOREIGN KEY(Product_ID) REFERENCES Product(Product_ID) ON DELETE CASCADE
);

CREATE TABLE CartItems(
	Customer_ID		INT,
	Product_ID		INT,
	Quantity		INT,

	PRIMARY KEY(Customer_ID, Product_ID),
	FOREIGN KEY(Customer_ID) REFERENCES Customer(Customer_ID) ON DELETE CASCADE,
	FOREIGN KEY(Product_ID) REFERENCES Product(Product_ID) ON DELETE CASCADE
	
);

CREATE TABLE Orders(
	Order_ID		INT IDENTITY(404000,1) PRIMARY KEY,
	PaymentMethod	VARCHAR(15) NOT NULL CHECK(PaymentMethod IN('COD','OnlinePayment')),
	TransactionID	BIGINT NOT NULL UNIQUE CHECK(LEN(TransactionID) = 16), --Only allows 16 digit ID
	DeliveryStatus	BIT NOT NULL,
	OrderDate		DATE NOT NULL DEFAULT GETDATE(),
	UpdatedAt		DATE NOT NULL DEFAULT GETDATE(),
	DispatchDate	DATE NOT NULL,
	DeliveryDate	DATE NOT NULL,
	DeliveryAddress INT FOREIGN KEY REFERENCES CustomerAddress(Address_ID) ,
	Customer_ID		INT FOREIGN KEY REFERENCES Customer(Customer_ID)
);


CREATE TABLE OrderItem(
	OrderItem_ID	INT IDENTITY(8090000,1) PRIMARY KEY,
	Quantity		INT NOT NULL CHECK(Quantity > 0),
	Product_ID		INT FOREIGN KEY REFERENCES Product(Product_ID) ON DELETE SET NULL,
	Order_ID		INT FOREIGN KEY REFERENCES Orders(Order_ID) ON DELETE CASCADE
);

CREATE TABLE CustomerServiceAgent(
	Agent_ID			INT IDENTITY(1,1) PRIMARY KEY,
	FirstName			VARCHAR(30) NOT NULL,
	LastName			VARCHAR(30)	NOT NULL,
	DateJoined			Date NOT NULL DEFAULT GETDATE(),
	Salary				INT	NOT NULL CHECK(Salary > 0),
	DisplayName			VARCHAR(30) NOT NULL,
	TotalQueriesHandled	INT NOT NULL DEFAULT 0 CHECK(totalQueriesHandled > 0),
);

CREATE TABLE CustomerServiceSession(
	Session_ID		INT IDENTITY(420000,1) PRIMARY KEY,
	SessionDate		DATE NOT NULL DEFAULT GETDATE(),
	StartTime		Time NOT NULL  DEFAULT (CONVERT(TIME, SYSDATETIME())),
	EndTime			Time NOT NULL,
	QueryType		VARCHAR(20) NOT NULL,
	Customer_ID		INT FOREIGN KEY REFERENCES Customer(Customer_ID) ON DELETE SET NULL,
	Agent_ID		INT FOREIGN KEY REFERENCES CustomerServiceAgent(Agent_ID) ON DELETE SET NULL

);

CREATE TABLE ProductReview(
	Comment				VARCHAR(255),
	Rating				INT	NOT NULL DEFAULT 0 CHECK(Rating >= 1 AND Rating <=5),
	ReviewDate			DATE NOT NULL DEFAULT GETDATE(),
	IsAnonymous			BINARY NOT NULL DEFAULT(0),
	Customer_ID			INT FOREIGN KEY REFERENCES Customer(Customer_ID) ON DELETE CASCADE,
	Product_ID			INT FOREIGN KEY REFERENCES Product(Product_ID) ON DELETE CASCADE,
	OrderItem_ID		INT FOREIGN KEY REFERENCES OrderItem(OrderItem_ID) ON DELETE CASCADE,

	PRIMARY KEY(OrderItem_ID)
);


--Finds number of customer in a certain range
SELECT count(customer_ID) AS totalCustomer
FROM Customer
WHERE DATEDIFF(YEAR, DateOfBirth,CONVERT(Date,SYSDATETIME())) BETWEEN 20 AND 30



